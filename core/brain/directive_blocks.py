def compose_system_prompt(profile_name: str) -> str:
    """Собрать system prompt для профиля (без merge_system / honesty)."""
    cfg = get_profile(profile_name)
    parts = [CORE_IDENTITY_RU, cfg.directive.strip()]
    tier = cfg.tier
    if tier == PromptTier.ULTRA_SHORT:
        return "\n\n".join(parts)
    if tier == PromptTier.LIGHT:
        parts.extend([TOOL_CALL_FORMAT, TOOLS_HINT_LIGHT, META_SELF_DISCLOSURE_RU])
        return "\n\n".join(parts)
    if tier == PromptTier.NORMAL:
        parts.extend(
            [TOOL_CALL_FORMAT, RULES_NORMAL_COMPACT, UNTRUSTED_EXTERNAL_COMPACT, META_SELF_DISCLOSURE_RU]
        )
        return "\n\n".join(parts)
    if tier == PromptTier.DEEP:
        parts.extend(
            [TOOL_CALL_FORMAT, RULES_DEEP_COMPACT, UNTRUSTED_EXTERNAL_COMPACT, META_SELF_DISCLOSURE_RU]
        )
        return "\n\n".join(parts)
    return "\n\n".join(parts)


def compose_system_prompt_standard() -> str:
    """Собрать system prompt для standard (с компактным honesty вместо merge_system)."""
    cfg = get_profile("standard")
    parts = [
        CORE_IDENTITY_RU,
        cfg.directive.strip(),
        TOOL_CALL_FORMAT,
        RULES_NORMAL_COMPACT,
        UNTRUSTED_EXTERNAL_COMPACT,
        HONESTY_COMPACT,
        META_SELF_DISCLOSURE_RU,
    ]
    return "\n\n".join(parts)


